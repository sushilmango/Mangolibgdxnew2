package net.mangoreader.gdx.data.model;

public class Image {

	private String created_at;
	private String deleted_at;
	private String id;
	private String media_filename;
	private String original_filename;
	private String updated_at;
	private String viewable_id;
	private String viewable_type;
	
	public String getCreated_at() {
		return created_at;
	}
	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}
	public String getDeleted_at() {
		return deleted_at;
	}
	public void setDeleted_at(String deleted_at) {
		this.deleted_at = deleted_at;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMedia_filename() {
		return media_filename;
	}
	public void setMedia_filename(String media_filename) {
		this.media_filename = media_filename;
	}
	public String getOriginal_filename() {
		return original_filename;
	}
	public void setOriginal_filename(String original_filename) {
		this.original_filename = original_filename;
	}
	public String getUpdated_at() {
		return updated_at;
	}
	public void setUpdated_at(String updated_at) {
		this.updated_at = updated_at;
	}
	public String getViewable_id() {
		return viewable_id;
	}
	public void setViewable_id(String viewable_id) {
		this.viewable_id = viewable_id;
	}
	public String getViewable_type() {
		return viewable_type;
	}
	public void setViewable_type(String viewable_type) {
		this.viewable_type = viewable_type;
	}
}
